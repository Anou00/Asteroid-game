bool init()
{
	//Initialization flag
	bool success = true;

	//Initialize SDL
	if (SDL_Init(SDL_INIT_VIDEO) < 0)
	{
		printf("SDL could not initialize! SDL Error: %s\n", SDL_GetError());
		success = false;
	}
	else
	{
		//Set texture filtering to linear
		if (!SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "1"))
		{
			printf("Warning: Linear texture filtering not enabled!");
		}

		//Create window
		//Create window
		SDL_Point pos{ SDL_WINDOWPOS_UNDEFINED, SDL_WINDOWPOS_UNDEFINED };
		SDL_Point size{ SCREEN_WIDTH, SCREEN_HEIGHT };
		gWindow = std::make_unique<Window>("SDL Tutorial", pos, size, SDL_WINDOW_SHOWN);

		if (gWindow == nullptr)
		{
			printf("Window could not be created! SDL Error: %s\n", SDL_GetError());
			success = false;
		}
		else
		{
			//Create vsynced renderer for window
			gRenderer = std::make_unique<Renderer>(gWindow.get()->get(), -1,
				SDL_RENDERER_ACCELERATED | SDL_RENDERER_PRESENTVSYNC);

			if (gRenderer == nullptr)
			{
				printf("Renderer could not be created! SDL Error: %s\n", SDL_GetError());
				success = false;
			}
			else
			{
				//Initialize renderer color
				SDL_SetRenderDrawColor(gRenderer.get()->get(), 0xFF, 0xFF, 0xFF, 0xFF);

				//Initialize PNG loading
				int imgFlags = IMG_INIT_PNG;
				if (!(IMG_Init(imgFlags) & imgFlags))
				{
					printf("SDL_image could not initialize! SDL_image Error: %s\n", IMG_GetError());
					success = false;
				}
			}
		}
	}

	return success;
}

bool loadMedia()
{
	//Loading success flag
	bool success = true;

	// Load asteroid image I got online
	if (!grock_image.loadFromFile("Assets/360x360 rock_image.jpg"))
	{
		printf("Failed to load image of asteroid!\n");
		success = false;
	}
	else {
	
		for (int i = 0; i < number_of_rocks_being_drawn; i++) {

			gSpriteClips[i].x = game.state.rocks[i].get_m_x();
			gSpriteClips[i].y = game.state.rocks[i].get_m_y();
			gSpriteClips[i].w = 270;
			gSpriteClips[i].h = 270;
		}
			
	}

	if (!gbullet_image.loadFromFile("Assets/asteroid game bullets.png"))
	{
		printf("Failed to load asteroid bullets texture!\n");
		success = false;
	}
	else {
		//Crop only one bullet that the asteroid will shoot.
		std::pair <int, int> x_and_y = game.state.player.get_tip_triangle_position();
		int bullet_x = x_and_y.first;
		int bullet_y = x_and_y.second;
		const int bullet_x_addition = 30;
		const int bullet_y_addition = 94;
		gbullet.x = bullet_x + bullet_x_addition;
		gbullet.y = bullet_y + bullet_y_addition;
		gbullet.w = 10;
		gbullet.h = 20;
	}

	return success;
}


void close()
{
	//Free loaded images
	gDotTexture.free();
	grock_image.free();
	gbullet_image.free();

	//Quit SDL subsystems
	IMG_Quit();
	SDL_Quit();
}

SDL_Texture* loadTexture(std::string path)
{
	//The final texture
	SDL_Texture* newTexture = nullptr;

	//Load image at specified path
	SDL_Surface* loadedSurface = IMG_Load(path.c_str());
	if (loadedSurface == nullptr)
	{
		printf("Unable to load image %s! SDL_image Error: %s\n", path.c_str(), IMG_GetError());
	}
	else
	{
		//Create texture from surface pixels
		newTexture = SDL_CreateTextureFromSurface(gRenderer.get()->get(), loadedSurface);
		if (newTexture == nullptr)
		{
			printf("Unable to create texture from %s! SDL Error: %s\n", path.c_str(), SDL_GetError());
		}

		//Get rid of old loaded surface
		SDL_FreeSurface(loadedSurface);
	}

	return newTexture;
}

LTexture::LTexture()
{
	//Initialize
	mTexture = nullptr;
	mWidth = 0;
	mHeight = 0;
}

LTexture::~LTexture()
{
	//Deallocate
	free();
}

bool LTexture::loadFromFile(std::string path)
{
	//Get rid of preexisting texture
	free();

	//The final texture
	SDL_Texture* newTexture = nullptr;

	//Load image at specified path
	SDL_Surface* loadedSurface = IMG_Load(path.c_str());
	if (loadedSurface == nullptr)
	{
		printf("Unable to load image %s! SDL_image Error: %s\n", path.c_str(), IMG_GetError());
	}
	else
	{
		//Color key image
		SDL_SetColorKey(loadedSurface, SDL_TRUE, SDL_MapRGB(loadedSurface->format, 0, 0xFF, 0xFF));

		//Create texture from surface pixels
		newTexture = SDL_CreateTextureFromSurface(gRenderer.get()->get(), loadedSurface);
		if (newTexture == nullptr)
		{
			printf("Unable to create texture from %s! SDL Error: %s\n", path.c_str(), SDL_GetError());
		}
		else
		{
			//Get image dimensions
			mWidth = loadedSurface->w;
			mHeight = loadedSurface->h;
		}

		//Get rid of old loaded surface
		SDL_FreeSurface(loadedSurface);
	}

	//Return success
	mTexture = newTexture;
	return mTexture != nullptr;
}


