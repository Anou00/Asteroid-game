void LTexture::free()
{
	//Free texture if it exists
	if (mTexture != nullptr)
	{
		SDL_DestroyTexture(mTexture);
		mTexture = nullptr;
		mWidth = 0;
		mHeight = 0;
	}
}

void LTexture::setColor(Uint8 red, Uint8 green, Uint8 blue)
{
	//Modulate texture rgb
	SDL_SetTextureColorMod(mTexture, red, green, blue);
}

void LTexture::setBlendMode(SDL_BlendMode blending)
{
	//Set blending function
	SDL_SetTextureBlendMode(mTexture, blending);
}

void LTexture::setAlpha(Uint8 alpha)
{
	//Modulate texture alpha
	SDL_SetTextureAlphaMod(mTexture, alpha);
}

void LTexture::render(int x, int y, SDL_Rect* clip, double angle, SDL_Point* center, SDL_RendererFlip flip)
{
	//Set rendering space and render to screen
	SDL_Rect renderQuad = { x, y, mWidth, mHeight };

	//Set clip rendering dimensions
	if (clip != nullptr)
	{
		renderQuad.w = clip->w;
		renderQuad.h = clip->h;
	}

	//Render to screen
	SDL_RenderCopyEx(gRenderer.get()->get(), mTexture, clip, &renderQuad, angle, center, flip);
}

int LTexture::getWidth()
{
	return mWidth;
}

int LTexture::getHeight()
{
	return mHeight;
}

#undef main
int main(int argc, char* argv[]) {
	//Start up SDL and create window
	if (!init())
	{
		printf("Failed to initialize!\n");
	}
	else
	{
		//Load media
		if (!loadMedia())
		{
			printf("Failed to load media!\n");
		}
		else
		{
			//Main loop flag
			bool quit = false;

			//Event handler
			SDL_Event e;

			// Random directions for rocks before while loop, so rocks directions are set once.
			game.random_direction_for_rocks();

			//While application is running
			while (!quit)
			{
				//Handle events on queue
				while (SDL_PollEvent(&e) != 0)
				{
					//User requests quit
					if (e.type == SDL_QUIT)
					{
						quit = true;
					}

					game.state.player.handleEvent(e);
					game.handleEvent_fire(e);

				}  

				int mouseX, mouseY;
				SDL_GetMouseState(&mouseX, &mouseY);

				game.update_rotation_of_player(mouseX, mouseY);

				// Handle player rotation based on mouse position
				game.state.player.handleMouseRotation(mouseX, mouseY);


				game.move_rocks_and_spawn_out_screen();
				int lives = game.move_player();

				game.movePositionXandYForPlayer();

				//If player loses all his lives game ends
				if (lives <= 0) quit = true;

				//Clear screen
				SDL_SetRenderDrawColor(gRenderer.get()->get(), 0xFF, 0xFF, 0xFF, 0xFF);
				SDL_RenderClear(gRenderer.get()->get());

				//render the rock images here so they're drawn above the player or dot when they collide.
				game.render_rocks(grock_image, gSpriteClips);

				//Render objects
				if (SDL_RenderGeometry(gRenderer.get()->get(), nullptr, game.state.player.triangleVertex, 3, nullptr, 0) < 0) { SDL_Log("%s\n", SDL_GetError()); }

				// Render the player 
				SDL_Vertex* vertices = game.state.player.getVertices();


				//bullets
				game.render_and_shoot_bullet(gbullet_image, gbullet);

				//Update screen
				SDL_RenderPresent(gRenderer.get()->get());

			}
		}
	}
	close();
	return 0;
}


